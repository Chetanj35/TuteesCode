package com.tutees.POJO;

public class FacultyBean {
	
	private String name;
	private String gender;
	private String bloodGroup;
	private String contact;
	private String email;
	private String password;
	private String collegeID;
	private String designation;
	private String coordinatorOf;
	private String subjectTeaching;
	private String classesTeaching;
	private String qualification;
	
	public FacultyBean() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getBloodGroup() {
		return bloodGroup;
	}

	public void setBloodGroup(String bloodGroup) {
		this.bloodGroup = bloodGroup;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getCollegeID() {
		return collegeID;
	}

	public void setCollegeID(String collageID) {
		this.collegeID = collageID;
	}

	public String getDesignation() {
		return designation;
	}

	public void setDesignation(String designation) {
		this.designation = designation;
	}

	public String getCoordinatorOf() {
		return coordinatorOf;
	}

	public void setCoordinatorOf(String coordinatorOf) {
		this.coordinatorOf = coordinatorOf;
	}

	public String getSubjectTeaching() {
		return subjectTeaching;
	}

	public void setSubjectTeaching(String subjectTeaching) {
		this.subjectTeaching = subjectTeaching;
	}

	public String getClassesTeaching() {
		return classesTeaching;
	}

	public void setClassesTeaching(String classesTeaching) {
		this.classesTeaching = classesTeaching;
	}

	public String getQualification() {
		return qualification;
	}

	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	
	
	

}
